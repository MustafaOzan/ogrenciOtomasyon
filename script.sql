 (OGR_ID,OGR_TC,OGR_ADI,OGR_SOYADI,CINSIYET,ZAMAN,ISLEM) 
 VALUES 
 (old.ID,old.OGR_TC,old.OGR_ADI,old.OGR_SOYADI,old.CINSIYET,
 datetime('now'),2); 
 END;